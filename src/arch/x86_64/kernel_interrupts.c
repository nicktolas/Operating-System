struct Call_Gate_Descriptor Int_Desc_Table_Entries[256] = {0};

void interrupts_init(void)
{
    idt_init();
    return;
}

void idt_init(void)
{
    // disable and remap pic
    // PIC_disable();
    PIC_remap(PIC_MREMAP, PIC_SREMAP);
    // fills idt with entries
    occupy_idt();
    // idt is currently intialized to zero
    load_idt();
    // configure the mask
    // enable interupts in PIC
    PIC_init();
    // enable interrupts
    return;
}

// interrupt to manage the keyboard reading
