// Enables interrupts post intitalization of the hardware itself
void enable_int_irq()
{
    IRQ_clear_mask(1); // Keyboard
    IRQ_clear_mask(2); // Cascade
    IRQ_clear_mask(4); // Serial COM1
    return;
}

//idt len should be byte size not entires length
void load_idt(void)
{
    static struct
    {
        uint16_t length;
        void* base;
    }__attribute__((packed)) idt;
    idt.length = sizeof(Int_Desc_Table_Entries);
    idt.base = Int_Desc_Table_Entries;
    asm ( "lidt %0" : : "m"(idt) );
    return;
}

void gen_isr_handler(int irq_num, int error_code)
{
    // debug
    CLI;
    switch (irq_num)
    {
        case 6: // Invalid Opcode
            printk("\r\nInvalid Opcode: %d\r\n", error_code);
            asm("hlt;");
            break;
        case 8: //Double Fault
            printk("\r\nDouble Fault: %d\r\n", error_code);
            asm("hlt");
            break;
        
        case 13: // general protection fault
            printk("\r\nGeneral Protection Fault: %d\r\n", error_code);
            asm("hlt;");
            break;

        case 14: //page fault
            printk("\r\nPage Fault: %d\r\n", error_code);
            // asm("hlt;");
            break;
        
        case 32: // Programmable Timer Interrupt
            // printk("\r\nProgrammable Timer Interrupt: %d\r\n", error_code);
            PIC_sendEOI(0);
            break;

        case 33: //Keyboard Interrupt
            keyboard_int_handler();
            PIC_sendEOI(1);
            break;
        
        case 36: // Serial Interrupt
            // printk("\r\nSerial Interrupt Encountered\r\n");
            serial_int_handler();
            PIC_sendEOI(4);
            break;
        
        case 128:// software interrupt
            printk("\r\nSoftware Interrupt Encountered\r\n");
            break;
        
        default:
            printk("\r\nUncaught Interrupt: %d\r\n", irq_num);
            asm("hlt;");
            break;
    }
    STI_post_CLI;
    return;
}

// PIC end of interupt call
void PIC_sendEOI(unsigned char irq)
{

	if(irq >= 8)
    {
        asm("out %0, %1; out %0, %2" : : "a"(PIC_EOI), "Nd"(PIC2_COMMAND), "Nd"(PIC1_COMMAND));
        // outb(PIC2_COMMAND,PIC_EOI);
    }
    else
    {
        asm volatile ( "out %0, %1" : : "a"(PIC_EOI), "Nd"(PIC1_COMMAND) );
        // outb(PIC1_COMMAND,PIC_EOI);
    }
}


void IRQ_set_mask(unsigned char IRQline)
{
    uint16_t port;
    uint8_t value;
 
    if(IRQline < 8) {
        port = PIC1_DATA;
    } else {
        port = PIC2_DATA;
        IRQline -= 8;
    }
    value = inb(port) | (1 << IRQline);
    outb(port, value);        
}
 
void IRQ_clear_mask(unsigned char IRQline)
{
    uint16_t port;
    uint8_t value;
 
    if(IRQline < 8) {
        port = PIC1_DATA;
    } else {
        port = PIC2_DATA;
        IRQline -= 8;
    }
    value = inb(port) & ~(1 << IRQline);
    outb(port, value);        
}

/*
arguments:
	offset1 - vector offset for master PIC
		vectors on the master become offset1..offset1+7
	offset2 - same for slave PIC: offset2..offset2+7
*/
void PIC_remap(int offset1, int offset2)
{
	unsigned char a1, a2;
 
	a1 = inb(PIC1_DATA);                        // save masks
	a2 = inb(PIC2_DATA);
 
	outb(PIC1_COMMAND, ICW1_INIT | ICW1_ICW4);  // starts the initialization sequence (in cascade mode)
	
	outb(PIC2_COMMAND, ICW1_INIT | ICW1_ICW4);
	
	outb(PIC1_DATA, offset1);                 // ICW2: Master PIC vector offset
	
	outb(PIC2_DATA, offset2);                 // ICW2: Slave PIC vector offset
	
	outb(PIC1_DATA, 4);                       // ICW3: tell Master PIC that there is a slave PIC at IRQ2 (0000 0100)
	
	outb(PIC2_DATA, 2);                       // ICW3: tell Slave PIC its cascade identity (0000 0010)
	
 
	outb(PIC1_DATA, ICW4_8086);
	
	outb(PIC2_DATA, ICW4_8086);
	
 
	outb(PIC1_DATA, a1);   // restore saved masks.
	outb(PIC2_DATA, a2);
}

/* Helper func */
uint16_t __pic_get_irq_reg(int ocw3)
{
    /* OCW3 to PIC CMD to get the register values.  PIC2 is chained, and
     * represents IRQs 8-15.  PIC1 is IRQs 0-7, with 2 being the chain */
    outb(PIC1_CMD, ocw3);
    outb(PIC2_CMD, ocw3);
    return (inb(PIC2_CMD) << 8) | inb(PIC1_CMD);
}
 
/* Returns the combined value of the cascaded PICs irq request register */
uint16_t pic_get_irr(void)
{
    return __pic_get_irq_reg(PIC_READ_IRR);
}
 
/* Returns the combined value of the cascaded PICs in-service register */
uint16_t pic_get_isr(void)
{
    return __pic_get_irq_reg(PIC_READ_ISR);
}



